if __name__ == '__main__':
    print("\n--- upper() ---")
    s1 = "Johny"
    print(s1.upper())

    print("\n--- lower() ---")
    print(s1.lower())

    print("\n--- isupper() ---")
    print(s1.isupper())
    print('abc'.isupper())
    print('ABC'.isupper())
    print('12345'.isupper())
    print('12345abc'.isupper())
    print('12345ABC'.isupper())

    print("\n--- islower() ---")
    print(s1.isupper())
    print('abc'.islower())
    print('ABC'.islower())
    print('12345'.islower())
    print('12345abc'.islower())
    print('12345ABC'.islower())
