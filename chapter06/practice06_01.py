if __name__ == '__main__':
    print("\n--- string literals ---")
    s1 = 'just a string'
    print(s1)

    print("\n--- double quotes ---")
    s2 = "don't underestimate double quotes because you can use single quotes inside them"
    print(s2)

    print("\n--- escape characters ---")
    s3 = "i was able to put a \t <- tab there"
    print(s3)

    print("\n--- raw strings ---")
    s4 = r"i was able to put a \t <- tab there"
    print(s4)

    print("\n--- multiline strings with triple quotes ---")
    s5 = """line1
    line2
line3"""
    print(s5)
